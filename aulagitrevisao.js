// Comandos git -> https://git-scm.com/docs/git

// git init -> inicia um repositório git local.

// git add . -> adiciona todos os arquivos do diretório atual ao repositório git local.

// git commit -m "mensagem do commit" -> cria um commit com a mensagem especificada.

// git push -> envia as alterações locais para o repositório remoto.

// Para dar o git push, é necessário ter um repositório remoto configurado.
// git remote add origin <url> -> adiciona um repositório remoto com o nome "origin" e a URL especificada.
// git push origin main -> envia as alterações locais para o branch "main" do repositório remoto "origin".

// git remote -v -> lista os repositórios remotos configurados.

// git pull -> busca as alterações do repositório remoto e as mescla com o repositório local.